							In de tijd voordat ik me aanmeldde op FOK! Lurkte ik vooral mee in SPT (waar de voetbaltopics destijds stonden) en ergerde ik me kapot aan al die domme voetbal meningen. Mijn mening was duidelijk superieur en ik had gelijk! Na maanden van ergeren heb ik me toch maar aangemeld om me in het discussie gedruis te storten. Al snel ontdekte ik dat er ook veel andere leuke subfora waren op FOK! Maar toch vooral SPT trok mijn aandacht. 
							<br /><br />
							Voetbal begon SPT wel erg te overheersen en ik was daarom begin 2003 erg blij met het tijdelijke VBL. Al snel vond ik ook daar mijn plaatsje. Heerlijk beetje lullen over voetbal, dat deed ik al met mijn vrienden maar ik kon nu met nog veel meer mensen in discussie. Heerlijk. Van een gekke Friese homo tot een eigenwijze Zeeuw die voor Ajax is. Met Ajacied die aan alle vooroordelen van arrogante Joden voldoen tot aan supporters die ongegeneerd van club verwisselden. Alles bleek er te vinden te zijn. Al snel voelde ik me er thuis. Zo thuis dat ik eens ging kijken in dat Hools, wat was dat nu? Er bleek over meer dan voetbal geluld te worden... maar eens per ongeluk wat gepost en al snel zat ik er middenin. Nu bleek ik echt bij het VBL kliekje te behoren <img src="/crew/img/smiley_emo.gif" />
							<br /><br />
							VBL groeide als kool en juni 2005 zou Mitt gaat stoppen als mod op VBL, er kwam een sollicitatie ronde! Na wat wikken en wegen heb ik destijds een sollicitatie ingestuurd. Onder de users was ik de favoriet; <a href="http://forum.fok.nl/topic/711314" target="_blank">Wie wordt de nieuwe Mod? Deeltje 2!</a> maar uiteindelijk werd het die schijnheilige Boschenaar <img src="/crew/img/smiley_redface.gif" /> <a href="http://forum.fok.nl/topic/712064!" target="_blank">Rubbereend wordt de nieuwe mod denk ik, #3!</a>
							<br /><br />
							Jammer maar helaas dus, volgende keer beter dacht ik nog. Toen kwam die volgende keer en bleek dat dekselse konijn me voor te zijn: <a href="http://forum.fok.nl/topic/748023" target="_blank">Afscheid van just me, welkom -Vaduz- op VBL</a>. Maar uiteindelijk mocht ik dan op 2 november (na de befaamde "zet je msn eens aan eend") DutchGooner opvolgen, <a href="http://forum.fok.nl/topic/773803" target="_blank">Mod-wissel op VBL!</a>.
							<br /><br />
							Later kwam Suijk er nog bij en draaide we een lekker team met Suijk, Vaduz en ik. De boel liep zoals we wilden en het ging er destijds naar onze mening goed aan toe in VBL en we werkten als team lekker samen. Maar dat hield in &eacute;&eacute;n keer op! Toen er op 9 augustus een "relletje" uitbrak op FOK! Waardoor veel modjes uit de crew werden gegooid. Ik heb toen besloten achter mijn collega's te gaan staan en uit solidariteit met Suijk en Vaduz mijn functie neer te leggen... En ineens ben je dan weer zwart <img src="/crew/img/smiley_biggrin.gif" />
							<br /><br />
							Dat was even wennen. Maar aan alles komt een eind en toen er in 2006 kerstgratie werd uitgesproken voor de stakingsmods werd mij gevraagd terug te komen. Daar heb ik meteen ja tegen gezegd. VBL liep niet helemaal lekker meer en ik wilde wel helpen! Uiteindelijk kwam dndiek er ook nog bij en zo hebben we met Raymania de laatste 8 maanden VBL proberen te veranderen. Dat heeft voor flinke discussies gezorgd achter de schermen maar gelukkig konden we met FA's samen komen tot een nieuw beleid. Dat doorvoeren was niet altijd even makkelijk. Ik kan me een wedstrijden van Ajax tegen Weder herinneren, dndiek was net begonnen en kreeg meteen een vuurdoop. Het ging er zo heftig aan toe dat Raymania en dndiek constant de topics hebben zitten doorlezen en editten en ik mocht de notes bijhouden en zorgen voor mailtjes/PM's en de banverzoeken bij de FA's. Ziektes, doodverwensingen en andere onvreindelijkheden vlogen je om de oren. Gelukkig hebben we die tijden nu inmiddels wel achter ons gelaten en staat er weer een stabiel VBL. Een VBL waar voor iedereen een plekje is en waar we prettig in discussie kunnen over onze gemeenschappelijke liefde voor Voetbal <img src="/crew/img/smiley_heart.gif" />
							<br /><br />
							Zo is VBL gegroeid van experiment naar &eacute;&eacute;n van de grootste subfora van het FOK!Forum en misschien wel het grootste voetbalforum van Nederland. Binnen FOK! worden we op waarde geschat en is de betrokkenheid van de FA's ook sterk gegroeid. Dat was iets wat me deze 2 mod periode opviel. En dat is fijn om te merken. VBL wordt volwassen!
